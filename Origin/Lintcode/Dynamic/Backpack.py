class Backpack():
    def no_value(self, m, A):
        A = [0]+A
        m = m+1
        dp_matrix = [[0 for j in range(m)]for i in range(len(A))]
        for i in range(1, len(A)):
            for j in range(1, m):
                if j-A[i] >= 0:
                    dp_matrix[i][j] = max(dp_matrix[i-1][j], dp_matrix[i-1][j-A[i]]+A[i])
                else:
                    dp_matrix[i][j] = dp_matrix[i-1][j]
        return dp_matrix[-1][-1]


